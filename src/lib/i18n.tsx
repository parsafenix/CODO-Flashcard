const messages: Record<UiLanguage, Record<string, string>> = {
  en: { ...en, ...enIntegrityOverrides },
  fa: { ...en, ...fa, ...faIntegrityOverrides },
  it: en,
};

const I18nContext = createContext<I18nContextValue | null>(null);

function formatMessage(template: string, params?: TranslationParams) {
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce(
    (result, [key, value]) => result.split(`{{${key}}}`).join(String(value)),
    template
  );
}

export function I18nProvider({ language, children }: PropsWithChildren<{ language: UiLanguage }>) {
  const value = useMemo<I18nContextValue>(() => {
    const dictionary = messages[language] ?? messages.en;
    return {
      language,
      isRtl: language === "fa",
      t: (key, params) => formatMessage(dictionary[key] ?? messages.en[key] ?? key, params),
    };
  }, [language]);

  useEffect(() => {
    document.documentElement.lang = language;
    document.documentElement.dir = language === "fa" ? "rtl" : "ltr";
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("I18n context is not available.");
  }
  return context;
}
